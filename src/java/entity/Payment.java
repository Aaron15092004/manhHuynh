package entity;

import java.time.LocalDateTime;

/**
 *
 * @author Admin
 */
public class Payment {
    private int paymentId;
    private int userId; // Foreign key to User
    private int eventId; // Foreign key to Event
    private int ticketId; // Foreign key to Ticket
    private double amount; // Số tiền thanh toán (USD)
    private LocalDateTime paymentDate;
    private String status; // PENDING, COMPLETED, FAILED

    public Payment() {
    }

    public Payment(int paymentId, int userId, int eventId, int ticketId, double amount, LocalDateTime paymentDate, String status) {
        this.paymentId = paymentId;
        this.userId = userId;
        this.eventId = eventId;
        this.ticketId = ticketId;
        this.amount = amount;
        this.paymentDate = paymentDate;
        this.status = status;
    }

    public int getPaymentId() {
        return paymentId;
    }

    public void setPaymentId(int paymentId) {
        this.paymentId = paymentId;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getEventId() {
        return eventId;
    }

    public void setEventId(int eventId) {
        this.eventId = eventId;
    }

    public int getTicketId() {
        return ticketId;
    }

    public void setTicketId(int ticketId) {
        this.ticketId = ticketId;
    }

    public double getAmount() {
        return amount;
    }

    public void setAmount(double amount) {
        this.amount = amount;
    }

    public LocalDateTime getPaymentDate() {
        return paymentDate;
    }

    public void setPaymentDate(LocalDateTime paymentDate) {
        this.paymentDate = paymentDate;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
    
}
